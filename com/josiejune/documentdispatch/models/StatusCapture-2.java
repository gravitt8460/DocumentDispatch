package com.josiejune.documentdispatch.models;


public class StatusCapture extends DDEntity  {

	public String getLog() {
		return log;
	}
	public  void setLog(String inLog) {
		log = inLog;
	}
	public  void addMessage (String message) {
		log = log + message + System.getProperty("line.separator");

	}

	public   String getFrom() {
		return from;
	}
	public  void setFrom(String inFrom) {
		from = inFrom;
	}
	public  String getNotes() {
		return notes;
	}
	public  void setNotes(String inNotes) {
		notes = inNotes;
	}
	private   String log = new String("");

	private  String from;
	private  String notes;
}
